     void Attshow_data(String value){
    String [] heads = {"sac","Student ID","Course code", "Perc of Attend"};
    String [] registers = new String [4];
    String sql = "select * from student_attendance where CONCAT (idstudent,' ',CourseNumb) LIKE '%" + value +"%'"  ;
    
    model = new DefaultTableModel(null,heads);
    
    vgc_students cc = new vgc_students();
    Connection cn = cc.conexion();
    
    Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()){
            registers[0]=rs.getString("sac");
            registers[1]=rs.getString("idstudent");
            registers[2]=rs.getString("CourseNumb");
            registers[3]=rs.getString("PercentageOfAttend");

            model.addRow(registers);
            }
            Att_Table.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
     
