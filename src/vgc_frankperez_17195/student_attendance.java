    private void Att_SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Att_SaveButtonActionPerformed
   vgc_students cc = new vgc_students();
   Connection cn = cc.conexion();  
   
      String stud_numb, course_numb, perc_numb ;
   String sql="";
   stud_numb = text_Attstud.getText();
   course_numb = text_Attcourse.getText();
   perc_numb = text_Attperc.getText();
 
sql = "INSERT INTO student_attendance (idstudent, CourseNumb,PercentageOfAttend) VALUES (?,?,?)";
 
       try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, stud_numb);
            pst.setString(2, course_numb);
            pst.setString(3, perc_numb);

           int n= pst.executeUpdate();
   if(n>0){
           JOptionPane.showMessageDialog(null, "Data Saved");
           Attactivate();
           Attshow_data("");
           }
            
        } catch (SQLException ex) {
            Logger.getLogger(students.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_Att_SaveButtonActionPerformed

