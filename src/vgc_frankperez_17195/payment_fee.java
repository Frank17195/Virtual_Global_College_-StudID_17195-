    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
    vgc_students cc = new vgc_students();
    Connection cn = cc.conexion();
    
   String stuid, totalam, dep, amdue, depdate;
   String sql="";
   stuid = txt_stuID.getText();
   totalam = txt_total_amount.getText();
   dep = txt_deposit.getText();
   amdue = txt_amount_due.getText();
   depdate = txt_depo_date.getText();
   sql = "INSERT INTO payment_of_fee (idstudent, TotalAmount, Deposit, AmountDue, Date_Of_Deposit) VALUES (?,?,?,?,?)";
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, stuid);
            pst.setString(2, totalam);
            pst.setString(3, dep);
            pst.setString(4, amdue);
            pst.setString(5, depdate);
           int n= pst.executeUpdate();
           if(n>0){
           JOptionPane.showMessageDialog(null, "Data Saved");
           show_data_payment("");
           }
            
        } catch (SQLException ex) {
            Logger.getLogger(students.class.getName()).log(Level.SEVERE, null, ex);
        }


    
// TODO add your handling code here:
    }//GEN-LAST:event_button_saveActionPerformed

