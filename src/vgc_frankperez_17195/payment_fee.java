    void show_data_payment(String value){
    String [] heads = {"Student ID","Total Amount","Deposit","Amount Due","Deposit Date"};
    String [] registers = new String [5];
    String sql = "select * from payment_of_fee where idstudent LIKE '%" + value +"%'"  ;
 
    model = new DefaultTableModel(null,heads);
    
    vgc_students cc = new vgc_students();
    Connection cn = cc.conexion();
    
        Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()){
            registers[0]=rs.getString("idstudent");
            registers[1]=rs.getString("TotalAmount");
            registers[2]=rs.getString("Deposit");
            registers[3]=rs.getString("AmountDue");
            registers[4]=rs.getString("Date_Of_Deposit");
            model.addRow(registers);
            }
            table_payment.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    
    
    }
    
    
